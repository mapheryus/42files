#include <stdio.h>

int	fact(int i)
{
	int	f;

	if (i == 1 || i == 0)
		return (1);
	f = i * fact(i - 1);
	return (f);
}

int	main(void)
{
	int	n = 10;
	printf ("%d", fact(n));
	return (0);
}
