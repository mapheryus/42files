#include <unistd.h>

void	print_num(int i)
{
	char	c;

	if(i >= 10)
		print_num(i / 10);
	c = (i % 10) + '0';
	write (1, &c, 1);
}

int	main(void)
{
	int	i = 10;

	print_num(i);
	return (0);
}
